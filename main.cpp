#include <iostream>

// [2024-08-16]
// 제목 : 연습문제

// 문제 10번 분석:
// 1. 2차 배열을 하드코딩하여 생성해야 한다.
// 2. 배열의 내용은 다음과 같다:
//    | 4 | 3 | 1 | 1 |
//    | 3 | 1 | 2 | 1 |
//    | 0 | 0 | 1 | 2 |
// 3. 사용자로부터 숫자를 입력받는다.
// 4. 입력받은 숫자가 2차 배열에 몇 개 존재하는지 세어야 한다.
// 5. 결과는 "X개 존재합니다" 형식으로 출력해야 한다.
// 6. 예를 들어, 1을 입력받으면 "5개 존재합니다"라고 출력해야 한다.


int main()
{
	int arr[3][4] = { {4, 3, 1, 1}, {3, 1, 2, 1}, {0, 0, 1, 2} };

	int input;
	std::cin >> input;

	int count = 0;
	for(int i = 0; i < 3; i++)
		for(int j = 0; j < 4; j++)
			if(arr[i][j] == input)
				count++;

	std::cout << count << "개 존재합니다." << std::endl;

	return 0;
}