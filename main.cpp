// 문제 8번
// 1. 3 x 5 크기의 문자 배열을 하드코딩해야 함.
// 2. 전역 배열을 사용하지 않고, 함수 내에서 배열을 정의해야 함.
// 3. 사용자가 입력한 문자 하나를 받아서, 해당 문자가 배열에 존재하는지 확인해야 함.
// 4. 존재 여부에 따라 "있음" 또는 "없음"을 출력해야 함.
// 5. CHECK 함수를 정의하여 문자가 배열에 있는지 확인하는 로직을 구현해야 함.
// 6. CHECK 함수는 char 타입의 인자를 받아야 하며, 배열을 순회하여 문자를 찾고,
//    찾으면 true를 반환하고, 찾지 못하면 false를 반환해야 함.
// 7. 결과는 main 함수에서 출력해야 함.


int main()
{
	char chArr[3][5] = {
		{'D', 'A', 'C', 'C', 'D'},
		{'S', 'D', 'F', 'A', 'E'},
		{'E', 'E', 'T', 'J', 'H'}
	};

	char ch;
	std::cout << "찾을 문자를 입력하세요 : ";
	std::cin >> ch;

	for(int i = 0; i < 3; i++)
		for(int j = 0; j < 5; j++)
			if(chArr[i][j] == ch)
			{
				std::cout << "있음" << std::endl;
				return 0;
			}
	std::cout << "없음" << std::endl;
	return 0;
}