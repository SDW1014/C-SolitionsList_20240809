// 문제 2번 
// 주어진 3x3 배열은 다음과 같습니다:
// | 1 | 1 | 1 |
// | 1 | 2 | 1 |
// | 3 | 6 | 3 |

// 이 배열을 전역변수로 사용하지 않고 하드코딩해야 합니다.
// 즉, 배열을 함수 내부에 정의해야 합니다.

// Count 함수는 입력받은 숫자가 배열 안에 몇 개 있는지를 세어야 합니다.
// 이 함수는 두 개의 중첩된 for 루프를 사용하여 배열의 모든 요소를 순회해야 합니다.
// 각 요소가 입력받은 숫자와 같은지 비교하고, 같으면 카운트를 증가시킵니다.
// 최종적으로 카운트를 반환해야 합니다.

// main 함수에서는 사용자로부터 숫자를 입력받고,
// Count 함수를 호출하여 그 결과를 출력해야 합니다.
// 입력받은 숫자는 Count 함수에 전달되어야 하며,
// Count 함수의 반환값을 출력해야 합니다.

int Count(int array[3][3], int x)
{
	int count = 0;
	for (int i = 0; i < 3; i++)
	{
		for (int j = 0; j < 3; j++)
			if (array[i][j] == x) count++;
	}
	return count;
}

int main()
{
	int array[3][3] = {
		{1, 1, 1},
		{1, 2, 1},
		{3, 6, 3}
	};

	int x;
	std::cout << "숫자를 입력하세요: ";
	std::cin >> x;

	std::cout << Count(array, x) << std::endl;

	return 0;
}