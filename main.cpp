// 문제 4번
// 3x3 배열을 하드코딩합니다.
// 배열의 값은 다음과 같습니다: 
// | 10 | 3  | 20 |
// | 60 | 30 | 40 |
// | 20 | 30 | 40 |
// 사용자로부터 두 개의 숫자 a와 b를 입력받습니다.
// a는 배열의 값보다 같거나 크고, b는 배열의 값보다 같거나 작아야 합니다.
// 조건을 만족하는 배열의 값의 개수를 세어서 출력합니다.

int arr[3][3] = {
	{10, 3, 20},
	{60, 30, 40},
	{20, 30, 40}
};

int main()
{
	int a, b;
	std::cout << "a와 b를 입력하세요: ";
	std::cin >> a >> b;

	int count = 0;
	for(int i = 0; i < 3; i++)
		for(int j = 0; j < 3; j++)
			if(arr[i][j] >= a && arr[i][j] <= b)
				count++;

	std::cout << "a와 b 사이에 있는 값의 개수: " << count << std::endl;

	return 0;
}