// 문제 1번
// 1. 변수 a, b를 선언하고 사용자로부터 두 개의 숫자를 입력받는다.
// 2. a부터 b까지의 숫자를 출력하기 위해 while 루프를 사용한다.
// 3. 루프는 a가 b보다 작거나 같을 때까지 반복된다.
// 4. 각 반복에서 현재 a 값을 출력하고, a를 1 증가시킨다.

int main()
{
	int a, b;
	std::cin >> a >> b;

	while (a <= b)
	{
		std::cout << a << " ";
		a++;
	}

	return 0;
}